# Средствами языка Python сформировать текстовый файл (.txt), содержащий
# последовательность из целых положительных и отрицательных чисел. Сформировать
# новый текстовый файл (.txt) следующего вида, предварительно выполнив требуемую
# обработку элементов:
# Исходные данные:
# Количество элементов:
# Индекс последнего минимального элемента:
# Сумма элементов больших 10 во второй половине:

# 1. Создаем исходный файл с числами
with open('numbers.txt', 'w', encoding='utf-8') as file:
    file.write("5 -3 10 15 -7 8 12 -10 20 6 11 -2 14")

# 2. Читаем данные из файла и обрабатываем их
with open('numbers.txt', 'r', encoding='utf-8') as file:
    numbers = list(map(int, file.read().split()))

# Вычисляем требуемые значения
count = len(numbers)  # Количество элементов
min_value = min(numbers)  # Минимальное значение
last_min_index = len(numbers) - 1 - numbers[::-1].index(min_value)  # Индекс последнего минимального элемента

# Сумма элементов >10 во второй половине
half = len(numbers) // 2
sum_gt_10 = sum(num for num in numbers[half:] if num > 10)

# 3. Записываем результаты в новый файл
with open('result.txt', 'w', encoding='utf-8') as file:
    file.write("Исходные данные: " + ' '.join(map(str, numbers)) + '\n')
    file.write(f"Количество элементов: {count}\n")
    file.write(f"Индекс последнего минимального элемента: {last_min_index}\n")
    file.write(f"Сумма элементов больших 10 во второй половине: {sum_gt_10}\n")

print("Файл result.txt успешно создан с результатами обработки.")
