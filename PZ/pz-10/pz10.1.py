# В магазинах имеются следующие товары. Магнит – молоко, соль, сахар. Пятерочка – мясо, молоко, сыр. Перекресток –
# молоко, творог, сыр, сахар. Определить:
# 1. в каких магазинах нельзя приобрести сыр.
# 2. в каких магазинах можно приобрести одновременно молоко и сахар.
# 3. в каких магазинах можно приобрести соль.

stores = {
    "Магнит": {"молоко", "соль", "сахар"},
    "Пятерочка": {"мясо", "молоко", "сыр"},
    "Перекресток": {"молоко", "творог", "сыр", "сахар"}
}

# 1. Магазины, где нельзя приобрести сыр
no_cheese_stores = [store for store, products in stores.items() if "сыр" not in products]
print("1. Магазины, где нельзя приобрести сыр:", no_cheese_stores)

# 2. Магазины, где есть одновременно молоко и сахар
milk_sugar_stores = [store for store, products in stores.items() if {"молоко", "сахар"}.issubset(products)]
print("2. Магазины с молоком и сахаром:", milk_sugar_stores)

# 3. Магазины, где можно приобрести соль
salt_stores = [store for store, products in stores.items() if "соль" in products]
print("3. Магазины с солью:", salt_stores)
