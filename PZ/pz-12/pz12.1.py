# В последовательности на n целых элементов найти количество пар, для которых
# произведение элементов делится на 3 (элементы пары в последовательности являются
# соседними).

# Ввод последовательности
n = int(input("Введите количество элементов последовательности: "))
sequence = []
for i in range(n):
    num = int(input(f"Введите {i+1}-й элемент: "))
    sequence.append(num)

# Поиск пар
count = 0
for i in range(len(sequence) - 1):
    # Проверяем, делится ли произведение соседних элементов на 3
    if (sequence[i] * sequence[i+1]) % 3 == 0:
        count += 1
        print(f"Найдена пара: ({sequence[i]}, {sequence[i+1]})")

# Вывод результата
print(f"\nОбщее количество пар, произведение которых делится на 3: {count}")